const router = require("express").Router();
const Post = require("../models/Posts");

//CREER UN COMMENTAIRE
router.post("/", async (req, res) => {
  const newPost = new Post(req.body);
  try {
    const savedPost = await newPost.save();
    res.status(200).json(savedPost);
  } catch (err) {
    res.status(500).json(err);
  }
});

//MISE A JOUR DU COMMENTAIRE
router.put("/:id", async (req, res) => {
  try {
    const post = await Post.findById(req.params.id);
    if (post.username === req.body.username) {
      try {
        const updatedPost = await Post.findByIdAndUpdate(
          req.params.id,
          {
            $set: req.body,
          },
          { new: true }
        );
        res.status(200).json(updatedPost);
      } catch (err) {
        res.status(500).json(err);
      }
    } else {
      res
        .status(401)
        .json("Vous pouvez modifier votre commentaire uniquement!");
    }
  } catch (err) {
    res.status(500).json(err);
  }
});

//SUPPRIMER UN COMMENTAIRE
router.delete("/:id", async (req, res) => {
  try {
    const post = await Post.findById(req.params.id);
    if (post.username === req.body.username) {
      try {
        await post.delete();
        res.status(200).json("le commentaire a etait supprimer.");
      } catch (err) {
        res.status(500).json(err);
      }
    } else {
      res
        .status(401)
        .json("Vous pouvez supprimer votre commentaire uniquement!");
    }
  } catch (err) {
    res.status(500).json(err);
  }
});

//OBTENIR UN COMMENTAIRE
router.get("/:id", async (req, res) => {
  try {
    const post = await Post.findById(req.params.id);
    res.status(200).json(post);
  } catch (err) {
    res.status(500).json(err);
  }
});

//OBTENIR TOUT LES COMMENTAIRES
router.get("/", async (req, res) => {
  const username = req.query.user;
  const catName = req.query.cat;
  try {
    let posts;
    if (username) {
      posts = await Post.find({ username });
    } else if (catName) {
      posts = await Post.find({
        categories: {
          $in: [catName],
        },
      });
    } else {
      posts = await Post.find();
    }
    res.status(200).json(posts);
  } catch (err) {
    res.status(500).json(err);
  }
});

module.exports = router;
